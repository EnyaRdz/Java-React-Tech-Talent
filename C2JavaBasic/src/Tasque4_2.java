public class Tasque4_2 {
	public static void main(String[] args) {
		//Declaracion y asignacion de variables
		int N = 40;
		double A = 15.5;
		char C = 'X';
		
		//Valores en la consola
		System.out.println("El valor de N es:" +N);
		System.out.println("En valor de A es:" +A);
		System.out.println("El valor de C es:" +C);
		
		System.out.println("Suma N + A = " + (N+A));
		System.out.println("Resta A - N =" + (A-N));
		System.out.println("Valor numerico de C = " + (int) C); //Conversion de String a ASCII
	}

}
